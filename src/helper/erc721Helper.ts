export const getERC721TotalSupply = async (provider: Provider, address: string) => {
  return (await getContract(provider, address, ERC721_ABI)).totalSupply();
};

export const getERC721OwnerOf = async (provider: Provider, address: string, id: number) => {
  return (await getContract(provider, address, ERC721_ABI)).ownerOf(id);
};

export const getERC721TokenURI = async (provider: Provider, address: string, id: number) => {
  return (await getContract(provider, address, ERC721_ABI)).tokenURI(id);
};

export const getERC721TokenOwnerOfEncode = (id: number) => {
  return encodeFunction(ERC721_ABI, 'ownerOf', [id]);
};

export const getERC721TokenOwnerOfDecode = (data: string) => {
  return decodeFunctionResultData(ERC721_ABI, 'ownerOf', data);
};

export const getERC721TokenURIEncode = (id: number) => {
  return encodeFunction(ERC721_ABI, 'tokenURI', [id]);
};

export const getERC721TokenURIDecode = (data: string) => {
  return decodeFunctionResultData(ERC721_ABI, 'tokenURI', data);
};

